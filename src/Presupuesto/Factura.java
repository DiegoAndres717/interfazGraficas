    //Metodo para hallar el total de la factura
    private void calcularTotal(){
        double suma = 0, iva, total;
        for (int i = 0; i < tblTabla.getRowCount(); i++) {
            suma = suma + Double.parseDouble(tblTabla.getValueAt(i, 3).toString());
        }
            suma = redonder(suma);
            lblSubTotal.setText(String.valueOf(suma));
            iva = suma * 0.19;
            iva = redonder(iva);
            lblIva.setText(String.valueOf(iva));
            total = suma + iva;
            total = redonder(total);
            lblTotal.setText(String.valueOf(total));
        
    }
    
    //Metodo para eliminar factura
    private void eliminarDato(){
        int fila;
        if (modelo.getRowCount() > 0) {
            if (tblTabla.getSelectedRow() != -1) {
                fila = tblTabla.getSelectedRow();
                modelo.removeRow(fila);
                calcularTotal();
            }else{
                JOptionPane.showMessageDialog(null, "Seleccione una fila de la factura");
            }
        }else{
            JOptionPane.showMessageDialog(null, "No hay filas en la tabla");
        }
    }
    
    //Metodo para redondear enteros
    private double redonder(double num){
        return Math.rint(num * 100)/100;
    }
